    private void cusIdsrchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cusIdsrchKeyReleased
        
           try {

                DefaultTableModel dtm = (DefaultTableModel) cus_table.getModel();
                dtm.setRowCount(0);
                if (!(cusIdsrch.getText().equals(""))) {
                    Statement con = dbConnection.db().createStatement();
                    ResultSet rs1 = con.executeQuery("SELECT * FROM customer_details WHERE cus_id LIKE'" + cusIdsrch.getText() + "%'");
                    while (rs1.next()) {
                        Vector v1 = new Vector();
                        v1.add(rs1.getString("cus_id"));
                        v1.add(rs1.getString("cus_name"));
                        dtm.addRow(v1);
                    }
                } else {
                    cusTable();
                }
            } catch (Exception ex) {
                Logger.getLogger(mainWindow.class.getName()).log(Level.SEVERE, null, ex);
                ex.printStackTrace();
            }
    }//GEN-LAST:event_cusIdsrchKeyReleased

