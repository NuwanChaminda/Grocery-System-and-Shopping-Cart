      public void cusTable() {
        DefaultTableModel table = (DefaultTableModel) cus_table.getModel();
        table.setRowCount(0);
        
        try {
            Statement con = dbConnection.db().createStatement();
            java.sql.ResultSet record =  con.executeQuery("SELECT * FROM customer_details" );
            while(record.next()){
                Vector rc = new Vector();
                rc.add(record.getString("cus_id"));
                rc.add(record.getString("cus_name"));
                table.addRow(rc);
            }
        } catch (Exception ex) {
            Logger.getLogger(mainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
   
    }
    
    
