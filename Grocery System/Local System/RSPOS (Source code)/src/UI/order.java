    private void order_tbl_ordersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_order_tbl_ordersMouseClicked
         try{
         Statement con = dbConnection.db().createStatement();
         DefaultTableModel table = (DefaultTableModel) order_tbl_qty.getModel();
         table.setRowCount(0);
         ResultSet record =  con.executeQuery("SELECT * \n" +
                                               "FROM order_details\n" +
                                               "INNER JOIN ordered_product ON ordered_product.order_id = order_details.order_id\n" +
                                               "INNER JOIN products ON products.product_id = ordered_product.product_id\n" +
                                               "WHERE order_details.order_id='"+order_tbl_orders.getValueAt(order_tbl_orders.getSelectedRow(), 0)+"'");
            while(record.next()){
            order_lbl_cusId.setText(record.getString("order_details.cus_id"));
            order_lbl_cusName.setText(record.getString("order_details.order_cus_name"));
            order_lbl_address.setText(record.getString("order_details.order_cus_address"));
            order_lbl_phoneNo.setText(record.getString("order_details.order_cus_phone"));
            order_lbl_DilDate.setText(record.getString("order_details.order_delivery_date"));
            order_lbl_DilTime.setText(record.getString("order_details.order_delivery_time"));
            order_lbl_msg.setText(record.getString("order_details.order_other_messages"));           
            order_lbl_anytime.setText(record.getString("order_details.delivery_anytime"));
                    
            //update ordered_products table(qty)
                 
                Vector rc = new Vector();
                rc.add(record.getString("ordered_product.product_id"));
                rc.add(record.getString("products.product_name"));
                rc.add(record.getString("ordered_product.quantity"));
                table.addRow(rc);
           
             if("Delivered".equals(record.getString("order_details.order_status"))){
                notify_delvrd.setVisible(true); 
                notify_notDel.setVisible(false);
                notify_cancelled.setVisible(false);
            }else if("Cancelled".equals(record.getString("order_details.order_status"))){
                notify_cancelled.setVisible(true);
                notify_notDel.setVisible(false);
                notify_delvrd.setVisible(false);
            }else{
                notify_cancelled.setVisible(false);
                notify_notDel.setVisible(true);
                notify_delvrd.setVisible(false);
            
            }  
           
            }
         }catch (Exception ex) {
            Logger.getLogger(mainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_order_tbl_ordersMouseClicked

