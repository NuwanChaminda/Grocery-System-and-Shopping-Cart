    public void OrdersTableUpdate() {
        DefaultTableModel table = (DefaultTableModel) order_tbl_orders.getModel();
        table.setRowCount(0);
        
        try {
            Statement con = dbConnection.db().createStatement();
            java.sql.ResultSet record =  con.executeQuery("SELECT * FROM order_details ORDER BY order_id DESC " );
            while(record.next()){
                Vector rc = new Vector();
                rc.add(record.getString("order_id"));
                rc.add(record.getString("order_dateTime"));
                
               
                table.addRow(rc);
            }
        } catch (Exception ex) {
            Logger.getLogger(mainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
   
    }
    
    
